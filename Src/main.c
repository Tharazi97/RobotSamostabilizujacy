/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  //
  //Inicjalizacja PWM, MPU-6050, filtru Kalmana, regulatora PID
  //

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

  HAL_I2C_Mem_Write(&hi2c1,ACC_ADDRESS,0x6B,1,&setting,1,100);//nastawy
  HAL_Delay(100);
  HAL_I2C_Mem_Write(&hi2c1,ACC_ADDRESS,0x6B,1,0x00,1,100);//wybudzenie
  HAL_Delay(1000);
  HAL_I2C_Mem_Write(&hi2c1,ACC_ADDRESS,0x1B,1,0x00,1,100);//zyroskop
  HAL_Delay(100);
  HAL_I2C_Mem_Write(&hi2c1,ACC_ADDRESS,0x1C,1,0x00,1,100);//akcelerometr
  HAL_Delay(100);

  KalmanFilter(0.001f, 0.003f, 0.03f);
  pid_init(6.0f,15.53f,0.0161f,0.005f);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  //
	  //Zczytanie danych z MPU-6050
	  //

	  //akcelerometr
	  HAL_I2C_Mem_Read(&hi2c1,ACC_ADDRESS,0x3D,1,&tab_read[0],2,100);
	  Yacc=((tab_read[0]<<8)|tab_read[1]);

	  HAL_I2C_Mem_Read(&hi2c1,ACC_ADDRESS,0x3B,1,&tab_read[0],2,100);
	  Xacc=((tab_read[0]<<8)|tab_read[1]);

	  if(Xacc!=0||Yacc!=0)
	  {
		  kat=atan2(Yacc,Xacc);
		  kat=kat*180/3.14f;
	  }

	  //zyroskop
   	  HAL_I2C_Mem_Read(&hi2c1,ACC_ADDRESS,0x47,1,&tab_read[0],2,100);
	  Zzyr=((tab_read[0]<<8)|tab_read[1]);
	  kat2=Zzyr*(-250)/32767;


	  //
	  //Przetworzenie danych przez filtr i podanie ich na regulator pid wraz z wymuszeniem
	  //

	  obr=KalmanUpdate(kat, kat2);
	  e=pid_compute(1.0f,obr);
	  h=(int8_t)e; //zrzutowanie float na int

	  //
	  //Podanie wartosci do odpowiednich rejestrow PWM. Offset dla pokonania pierwszych oporow rozruchu silnika
	  //

	  if(h>0)
	  {
		  TIM2->CCR3 = 0;
		  TIM2->CCR4 = 10+h;
	  }
	  if(h<0)
	  {
		  TIM2->CCR4 = 0;
		  TIM2->CCR3 = 10+((-1)*h);
	  }
	  if(h==0)
	  {
		  TIM2->CCR4 = 0;
		  TIM2->CCR3 = 0;
	  }


   	  HAL_Delay(5);


  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

